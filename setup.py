def check_python_version():
    """Check if Python version is 3.8 or higher"""
    if sys.version_info < (3, 8):
        print("âŒ Python 3.8 or higher is required.")
        print(f"   Current version: {sys.version}")
        sys.exit(1)
    print(f"âœ… Python version: {sys.version.split()[0]}")


def create_env_file():
    """Create .env file from template"""
    env_path = Path('.env')
    template_path = Path('config.env.template')
    
    if env_path.exists():
        print("âœ… .env file already exists")
        return
    
    if not template_path.exists():
        print("âš ï¸  config.env.template not found, creating basic .env file...")
        
        # Generate a secure secret key
        secret_key = secrets.token_hex(32)
        
        env_content = f"""# Email Summarizer Environment Configuration
# Generated by setup script

# Flask Configuration
FLASK_APP=run.py
FLASK_ENV=development
SECRET_KEY={secret_key}

# Database Configuration
DATABASE_URL=sqlite:///email_summarizer.db

# Microsoft Azure/Graph API Configuration
AZURE_CLIENT_ID=your-azure-client-id
AZURE_CLIENT_SECRET=your-azure-client-secret
AZURE_TENANT_ID=common
REDIRECT_URI=http://localhost:5000/auth/callback

# OpenAI Configuration
OPENAI_API_KEY=your-openai-api-key
OPENAI_MODEL=gpt-3.5-turbo

# Admin Configuration
ADMIN_DEFAULT_PASSWORD=changeme123

# Application Settings
APP_NAME=Email Summarizer
APP_VERSION=1.0.0
MAX_EMAILS_PER_DIGEST=200
# DAILY_DIGEST_LIMIT=1  # Deprecated - no limit
"""
        with open('.env', 'w') as f:
            f.write(env_content)
        print("âœ… Created .env file with default configuration")
    else:
        # Copy from template
        import shutil
        shutil.copy(template_path, env_path)
        print("âœ… Created .env file from template")
    
    print("\nâš ï¸  IMPORTANT: Edit .env file and add your API credentials:")
    print("   - AZURE_CLIENT_ID and AZURE_CLIENT_SECRET")
    print("   - OPENAI_API_KEY")
    print("   - Change ADMIN_DEFAULT_PASSWORD")


def create_directories():
    """Create necessary directories"""
    directories = [
        'logs',
        'instance',
        'migrations',
        'app/static/css',
        'app/static/js',
        'app/static/img',
    ]
    
    for directory in directories:
        Path(directory).mkdir(parents=True, exist_ok=True)
    
    print("âœ… Created directory structure")


def install_requirements():
    """Install Python requirements"""
    print("\nðŸ“¦ Installing Python packages...")
    try:
        subprocess.check_call([sys.executable, '-m', 'pip', 'install', '--upgrade', 'pip'])
        subprocess.check_call([sys.executable, '-m', 'pip', 'install', '-r', 'requirements.txt'])
        print("âœ… Python packages installed successfully")
    except subprocess.CalledProcessError:
        print("âŒ Failed to install Python packages")
        print("   Try running: pip install -r requirements.txt")
        return False
    return True


def initialize_database():
    """Initialize the database"""
    print("\nðŸ—„ï¸  Initializing database...")
    try:
        # Set minimal environment variables for database initialization
        os.environ['FLASK_APP'] = 'run.py'
        os.environ['FLASK_ENV'] = 'development'
        
        # Run database initialization
        subprocess.check_call([sys.executable, 'init_db.py'])
        return True
    except subprocess.CalledProcessError:
        print("âŒ Failed to initialize database")
        print("   Try running: python init_db.py")
        return False


